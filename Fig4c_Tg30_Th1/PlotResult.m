%% load start and end positions of crustal nodes

filename = 'T15_Start.rpt';
startRow = 14;
endRow = 11107;
formatSpec = '%*16s%16f%f%[^\n\r]';
fileID = fopen(filename,'r');
textscan(fileID, '%[^\n\r]', startRow-1, 'WhiteSpace', '', 'ReturnOnError', false);
dataArray = textscan(fileID, formatSpec, endRow-startRow+1, 'Delimiter', '', 'WhiteSpace', '', 'TextType', 'string', 'EmptyValue', NaN, 'ReturnOnError', false, 'EndOfLine', '\r\n');

fclose(fileID);

X0 = dataArray{:, 1};
Y0 = dataArray{:, 2};

clearvars filename startRow endRow formatSpec fileID dataArray ans;


filename = 'T464_End.rpt';
startRow = 14;
endRow = 11107;

formatSpec = '%*16s%16f%f%[^\n\r]';

fileID = fopen(filename,'r');

textscan(fileID, '%[^\n\r]', startRow-1, 'WhiteSpace', '', 'ReturnOnError', false);
dataArray = textscan(fileID, formatSpec, endRow-startRow+1, 'Delimiter', '', 'WhiteSpace', '', 'TextType', 'string', 'EmptyValue', NaN, 'ReturnOnError', false, 'EndOfLine', '\r\n');

